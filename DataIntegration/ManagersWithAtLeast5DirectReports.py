import pandas as pd


# Runtime
# Details
# 548ms
# Beats 6.25%of users with Pandas
# Memory
# Details
# 63.34MB
# Beats 7.66%of users with Pandas
def find_managers(employee: pd.DataFrame) -> pd.DataFrame:
    managers = employee.groupby('managerId').nunique().reset_index().query('id >= 5')[['managerId']]
    return managers.merge(employee, how='left', left_on='managerId', right_on='id')[['name']].dropna()


data = [[101, 'John', 'A', None], [102, 'Dan', 'A', 101], [103, 'James', 'A', 101], [104, 'Amy', 'A', 101], [105, 'Anne', 'A', 101], [106, 'Ron', 'B', 101]]
Employee = pd.DataFrame(data, columns=['id', 'name', 'department', 'managerId']).astype({'id':'Int64', 'name':'object', 'department':'object', 'managerId':'Int64'})


print(find_managers(Employee))
